function calculateTotals() {
    const subTotalEl = document.getElementById('subTotal');
    const grandTotalEl = document.getElementById('grandTotal');
    const globalDiscInput = document.getElementById('globalDiscount');

    if (!subTotalEl || !grandTotalEl) return;

    const subtotal = quoteItems.reduce((sum, item) => {
        if (item.type === 'section') return sum;
        return sum + (item.price || 0);
    }, 0);

    const ad = globalDiscInput ? parseFloat(globalDiscInput.value) : 0;
    const additionalDisc = isNaN(ad) ? 0 : ad;
    const combinedDiscount = additionalDisc;

    const finalTotal = subtotal * (1 - (combinedDiscount / 100));

    subTotalEl.textContent = formatCurrency(subtotal);
    grandTotalEl.textContent = formatCurrency(finalTotal);
}
window.calculateTotals = calculateTotals;

