/**
 * Global search handler for the Ecolab-style home search bar.
 * Routes the query to the most relevant tool tab.
 */
function handleGlobalSearch(query) {
    if (!query || !query.trim()) return;
    const q = query.trim().toLowerCase();
    const tabBtns = document.querySelectorAll('.tab-btn');
    const sections = document.querySelectorAll('.tab-content');

    let targetTab = 'catsheets'; // default: search documents

    // SDS keywords
    if (q.includes('sds') || q.includes('safety') || q.includes('an toàn') || q.includes('msds')) {
        targetTab = 'sds';
    }
    // Quotation keywords
    else if (q.includes('giá') || q.includes('quotation') || q.includes('báo giá') || q.includes('price')) {
        targetTab = 'quotation';
    }
    // Proposal keywords
    else if (q.includes('proposal') || q.includes('đề xuất') || q.includes('hợp tác')) {
        targetTab = 'proposal';
    }

    // Navigate to the target tab first
    const targetBtn = document.querySelector(`[data-tab="${targetTab}"]`);
    if (targetBtn) {
        tabBtns.forEach(b => b.classList.remove('active', 'text-slate-900', 'bg-white', 'shadow-sm'));
        tabBtns.forEach(b => b.classList.add('text-slate-500', 'hover:text-primary'));
        targetBtn.classList.add('active', 'text-slate-900', 'bg-white', 'shadow-sm');
        targetBtn.classList.remove('text-slate-500', 'hover:text-primary');
        sections.forEach(sec => sec.classList.remove('active'));
        const targetSection = document.getElementById(targetTab);
        if (targetSection) targetSection.classList.add('active');
        window.scrollTo({ top: 0, behavior: 'smooth' });
    }

    // Pre-fill the search input in the target tab
    setTimeout(() => {
        const sdsInput = document.getElementById('sds-search-input');
        const catInput = document.getElementById('catsheet-search');
        if (targetTab === 'sds' && sdsInput) {
            sdsInput.value = query;
            sdsInput.dispatchEvent(new Event('input'));
        } else if (targetTab === 'catsheets' && catInput) {
            catInput.value = query;
            catInput.dispatchEvent(new Event('input'));
        }
    }, 100);
}
window.handleGlobalSearch = handleGlobalSearch;

