function removeSection(sectionId) {
    const id = Number(sectionId);
    const index = quoteItems.findIndex(i => i.id === id);
    if (index !== -1) {
        // Find next section
        let nextSectionIndex = -1;
        for (let i = index + 1; i < quoteItems.length; i++) {
            if (quoteItems[i].type === 'section') {
                nextSectionIndex = i;
                break;
            }
        }
        if (nextSectionIndex === -1) nextSectionIndex = quoteItems.length;

        quoteItems.splice(index, nextSectionIndex - index);
        renderRows(true);
    }
}
