function applyLanguage(lang) {
    const t = translations[lang];
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (t[key]) el.innerHTML = t[key];
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.dataset.i18nPlaceholder;
        if (t[key]) el.placeholder = t[key];
    });

    // Translate Proposal Maker placeholders specifically
    const pdfClientInput = document.getElementById('pdfClientName');
    const pdfRecipientInput = document.getElementById('pdfRecipient');
    if (pdfClientInput && t.ph_client_name) pdfClientInput.placeholder = t.ph_client_name;
    if (pdfRecipientInput && t.ph_recipient) pdfRecipientInput.placeholder = t.ph_recipient;

    // Translate dynamic search areas
    const libEmptyEl = document.querySelector('#libraryResults .text-center');
    if (libEmptyEl && libEmptyEl.innerText.trim() !== "" && t.lib_empty) {
        // Only update if it's the initial empty message
        if (libEmptyEl.dataset.i18n === 'lib_empty') {
            libEmptyEl.innerText = t.lib_empty;
        } else if (libEmptyEl.dataset.i18n === 'lib_no_results') {
            libEmptyEl.innerText = t.lib_no_results;
        }
    }

    // Translate standard categories in quoteItems
    quoteItems.forEach(item => {
        if (item.type === 'section') {
            const hkNames = [translations.en.cat_hk, translations.vn.cat_hk];
            const kiNames = [translations.en.cat_ki, translations.vn.cat_ki];
            const laNames = [translations.en.cat_la, translations.vn.cat_la];

            if (hkNames.includes(item.name.toUpperCase())) {
                item.name = t.cat_hk;
            } else if (kiNames.includes(item.name.toUpperCase())) {
                item.name = t.cat_ki;
            } else if (laNames.includes(item.name.toUpperCase())) {
                item.name = t.cat_la;
            }
        }
    });

    renderRows();
    renderDocs();
}

